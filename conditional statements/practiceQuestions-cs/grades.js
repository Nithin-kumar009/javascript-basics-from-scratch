let marks= prompt("enter student marks");
if(marks>=80 && marks<=100){
    console.log("this student got A grade");
}
else if(marks>=70 && marks<=89){
    console.log("this student got B grade");
}
else if(marks>=60 && marks<=69){
    console.log("this student got C grade")
}
else if(marks>=50 && marks<=59){
    console.log("this student got D grade");

}
else{
    console.log("this student is failed");

}