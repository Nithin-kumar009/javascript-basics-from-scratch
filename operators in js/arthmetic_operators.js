//arthemetic operators
let a=5;
let b=3;
//basic
console.log("a+b=",a+b);
console.log("a-b=",a-b);
console.log("a*b=",a*b);
console.log("a/b=",a/b);
//additional
console.log("a%b=",a%b); //modulus for reminder
console.log("a**b=",a**b); //exponent or power

//unary operators
a++; //post increment the value is equal to a+1 
++a; //pre increment the value is equal to 1+a
a--;//post decrement = a-1
--a; //pre decrement = 1-a
console.log("a++=",a++); //output is 5 because it prints on next line
console.log("++a=",++a); // out put is 7 because it has a++ makes a=6 and ++a again adds 1 so output is 7
console.log("a--=",a--); //out put is 7 because it decreases on next line
console.log("--a=",--a); //output is 5 because has already decreased by 1 and again 1 .so, 7-1-1=5