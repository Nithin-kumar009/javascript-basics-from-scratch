//string methods are built in functions to manipulate a string
let name=" nithinKUMAR ";
let name2=" Saniya"
console.log(name.toUpperCase()); //str.toUpperCase - converts string into uppercase
console.log(name.toLowerCase()); //str.toLowerCase - converts string into lower case
console.log(name.trim()); //trims the white spaces in a string
console.log(name.slice(2,7)); //slices the string based on the index
console.log(name.concat(name2)); //joins string 1 with string 2
console.log(name.replace("KUMAR","saniya")); //replaces one string or chracter with another string or character - replaces only first match
console.log(name.replaceAll("n","s")); //replaces all matching values
console.log(name.charAt(4)); // str.charAt(idx) - prints only selected index valued character
 